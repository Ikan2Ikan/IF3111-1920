    <p>Jumlah pelapor per Januari 2019 adalah sebanyak 801.257 pengguna. Total laporan yang telah masuk sebanyak 1.389.891. Sumber laporan terbanyak melalui website diikuti oleh SMS, twitter dan aplikasi mobile</p>

    <p> Fitur-fitur yang ada dalam SP4N-LAPOR!
