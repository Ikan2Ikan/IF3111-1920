     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(report $report)
    {
       report::destroy($report->id);
       return redirect('/')->with('status', 'Laporan berhasil di hapus !');
        
